    private int getRandomColor() {
        ArrayList<Integer> colors = new ArrayList<Integer>();
        boolean available;

        // Add all available colors
        for (int i=0; i<3; i++) {
            available = true;
            for (Player player : players) {
                if (player.getColor() == i) {
                    available = false;
                    break;
                }
            }
            if (available)
                colors.add(i);
        }
        return colors.get(rnd.nextInt(colors.size()));
    }

